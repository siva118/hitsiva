package com.technoelevate.basicprograms;



public class Array_with_Scanner {
	public static int a;
	public static void main(String[] args) {
		
		Array_with_Scanner s=new Array_with_Scanner();

				s.siva(10);
		System.out.println(s.mark());
		
	}
	public void siva(int n) {
		
		a=n;
	}
public int mark() {
		
		return a;
	}
	
	

}
